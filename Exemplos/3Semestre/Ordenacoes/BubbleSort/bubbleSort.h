/**
 * @file bubbleSort.h
 * @brief Bubble Sort
*/

#ifndef _BUBBLE_SORT
#define _BUBBLE_SORT

/**
 * @brief Ordena um vetor de inteiros utilzando o Bubble Sort
 * @param v Endereço de um vetor de inteiros
 * @param n O número de elementos do vetor
*/
void bubbleSort(int* v, int n);

/**
 * @brief Ordena um vetor de inteiros utilzando o Bubble Sort de forma otimizada
 * @param v Endereço de um vetor de inteiros
 * @param n O número de elementos do vetor
*/
void bubbleSortOtimizado(int* v, int n);

#endif